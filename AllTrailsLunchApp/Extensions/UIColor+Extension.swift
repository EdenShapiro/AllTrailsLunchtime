    static var starYellow: UIColor { return UIColor(named: "star-yellow")! }
    static var graySubtitleText: UIColor { return UIColor(named: "gray-subtitle-text")! }
    static var darkGrayText: UIColor { return UIColor(named: "dark-gray-text")! }
}


